package com.dacheng.mes.sfdcs.pojo;



public class Process {
	
	private int processKey;
	
	private String processName;
	
	private int emvControlled;

	public int getProcessKey() {
		return processKey;
	}

	public void setProcessKey(int processKey) {
		this.processKey = processKey;
	}

	public String getProcessName() {
		return processName;
	}

	public void setProcessName(String processName) {
		this.processName = processName;
	}

	public int getEmvControlled() {
		return emvControlled;
	}

	public void setEmvControlled(int emvControlled) {
		this.emvControlled = emvControlled;
	}


}
